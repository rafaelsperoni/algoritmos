#Faça um algoritmo que calcule a área de um terreno. Os terrenos são retangulares, e deverão ser informadas as medidas de largura e comprimento do terreno.
larg = float(input("Informe a largura, em metros: "))
comp = float(input("Informe o comprimento, em metros: "))

area = larg * comp

print("A área do terreno: ", area, " m2.")