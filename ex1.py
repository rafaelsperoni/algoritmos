# Faça um algoritmo que 
# leia três números, 
# calcule a soma deles e 
# escreva o resultado.

#entradas
n1 = int(input("Informe primeiro número: "))
n2 = int(input("Informe segundo número: "))
n3 = int(input("Informe terceiro número: "))

#processamento
soma = n1+n2+n3

#saida
print("A soma é: ", soma)
print("Fim do algoritmo")